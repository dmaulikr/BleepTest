import UIKit
import Hex

extension UIColor {
    class func customColor() -> UIColor {
        return UIColor(hex: "FF4B47")
    }
    class func italyGreenColor() -> UIColor {
        return UIColor(hex: "#009246")
    }
    class func italyBrownColor() -> UIColor {
        return UIColor(hex :"#653606")
    }
}
