import UIKit
import Hex

extension UIColor {
    class func customColor() -> UIColor {
        return UIColor(hex: "FF4B47")
    }
    class func italyGreenColour() -> UIColor {
        return UIColor(hex: "#009246")
    }
    class func italyRedColor() -> UIColor{
        return UIColor(hex: "#ce2b37")
    }
}
