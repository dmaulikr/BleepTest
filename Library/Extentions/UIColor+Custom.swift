import UIKit
import Hex

extension UIColor {
    class func customGreenColor() -> UIColor {
        return UIColor(hex: "#86c06d")
    }
    class func customBlueColor() -> UIColor {
        return UIColor(hex: "#4a86e8")
    }
    class func customLightBlueColor() -> UIColor {
        return UIColor(hex: "#72c8ed")
    }
    class func pinkRedColor() -> UIColor {
        return UIColor(hex: "#e06666")
    }
    class func customRedColor() -> UIColor {
        return UIColor(hex: "#cc0000")
    }
    class func veryLightGrayColor() -> UIColor {
        return UIColor(hex :"#f2f2f2")
    }
}
